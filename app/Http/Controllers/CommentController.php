    public function switchVisible(Request $request)
    {
        if($request->ajax()){
            $comment=Comment::where('id',$request->comment)->first();
            $switch = $comment->visible===1 ? 0:1;
            $comment->visible=$switch;
            $comment->save();


        }
        return response()->json('ok',200);
    }


